package com.callor.varriable;

public class Var_02 {
	public static void main(String[] args) {
		
		int num1 = 0;
		num1 = num1 +10;
		num1 += 10;
		//위의 두 명령문은 같은 명령문
		
		num1 = num1 - 10;
		num1 -= 10;
		//+와 -는 =의 앞뒤 어디든 상관 없음
		
		num1 = num1 * 10;
		num1 *= 10;
		
		num1 = num1 / 10;
		num1 /= 10;
		
		num1 = num1 % 10;
		num1 %= 10;
		
		//나머지 연산은 =의 앞에
				
		
		
	}

}
